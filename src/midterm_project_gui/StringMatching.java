    // String matching algorithm
    public static String[] searchWordInString(String address, String word) {
        // Convert both text and word to lowercase to make the search case-insensitive
        String lowerText = address.toLowerCase();
        String lowerWord = word.toLowerCase();

        int wordCount = 0;
        List<Integer> positions = new ArrayList<>();

        // Split the lowerText into words using space as delimiter
        String[] words = lowerText.split(" ");
        
        for (int i = 0; i < words.length; i++) {
            if (words[i].equals(lowerWord)) {
                wordCount++;
                positions.add(i + 1); // Add 1 to make position human-readable (starting from 1 instead of 0)
            }
        }

        // Convert wordCount and positions to strings
        String wordCountString = Integer.toString(wordCount);
        String wordPositionString = positions.isEmpty() ? "" : String.join(", ", positions.stream().map(Object::toString).toArray(String[]::new));

        return new String[]{wordCountString, wordPositionString};
    }

