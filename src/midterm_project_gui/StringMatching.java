    private void Search_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_ButtonActionPerformed
        
        if(this.Search_Input.getText().isBlank()){
            JOptionPane.showMessageDialog(this,
                "Missing word to search!",
                "Error", // Title of the dialog
                JOptionPane.ERROR_MESSAGE); // Message type
        }
        
        else{
            String address = this.Address_Input.getText();
            String word = this.Search_Input.getText();
            
            address = address.replace(",", ""); // Remove the commas in address before string matching
            
            String[] result = searchWordInString(address, word);
            this.Occurences_Input.setText(result[0]);
            this.Position_Input.setText(result[1]);
        }        
    }//GEN-LAST:event_Search_ButtonActionPerformed

